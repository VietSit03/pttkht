    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Orders bill = new Orders();
        bill.setId(Controller.OrderControl.ReturnNextIDO());
        int rowCount = modelO.getRowCount();
        for (int i = 0; i <= rowCount - 1; i++) {
            String idD = Controller.DrinkControl.findByName(jTableOrder.getValueAt(i, 0).toString()).getId();
            int q = Integer.parseInt(jTableOrder.getValueAt(i, 1).toString());
            int p = Integer.parseInt(jTableOrder.getValueAt(i, 3).toString());
            String note = jTableOrder.getValueAt(i, 4).toString();
            OrderDetails e = new OrderDetails(bill.getId(), idD, q, p, note);
            Controller.OrderControl.addOD(e);
        }
        bill.setDate(LocalDateTime.now());
        bill.setIdEmp(e.getId());
        bill.setStatus(1);
        Controller.OrderControl.add(bill);
        this.setVisible(false);
        new OrderBill(modelO, bill).show();
    }//GEN-LAST:event_jButton1ActionPerformed


    public void showTable() {
        for (Drink d : drink) {
            modelD.addRow(new Object[]{
                d.getName(), d.getType(), d.getPrice(), Controller.PromotionControl.getPriceP(d)
            });
        }
    }

    public void showOU() {
        int rowCountLO = modelLO.getRowCount();
        // Xoá từng hàng một
        for (int i = rowCountLO - 1; i >= 0; i--) {
            modelLO.removeRow(i);
        }
        int rowCountOD = modelOD.getRowCount();
        // Xoá từng hàng một
        for (int i = rowCountOD - 1; i >= 0; i--) {
            modelOD.removeRow(i);
        }
        oL = Controller.OrderControl.listOU(username);
        for (Orders o : oL) {
            modelLO.addRow(new Object[]{
                o.getId(), o.getDate(), o.getDate().plusMinutes(10)
            });
        }
    }

    public void showDongCa() {
        int rowCountLO = modelDC.getRowCount();
        // Xoá từng hàng một
        for (int i = rowCountLO - 1; i >= 0; i--) {
            modelDC.removeRow(i);
        }
        int p = 0;
        LOD = Controller.OrderControl.listOrderDay(e);
        for (Orders o : LOD) {
            modelDC.addRow(new Object[]{
                o.getId(), o.getIdEmp(), o.getDate(), Controller.OrderControl.convertStatus(o.getStatus()), Controller.OrderControl.convertMethod(o.getMethodPay()), Controller.OrderControl.getPrice(o)
            });
            p += Controller.OrderControl.getPrice(o);
        }
        jLabelDTC.setText(String.valueOf(p));
    }

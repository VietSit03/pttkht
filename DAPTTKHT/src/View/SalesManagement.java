    private void jTableLOMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableLOMouseClicked
        // TODO add your handling code here:
        int rowCount = modelOD.getRowCount();
        // Xoá từng hàng một
        for (int i = rowCount - 1; i >= 0; i--) {
            modelOD.removeRow(i);
        }
        for (Orders o : oL) {
            if (jTableLO.getValueAt(jTableLO.getSelectedRow(), 0).toString().equals(o.getId())) {
                od = o;
                for (OrderDetails oD : Controller.OrderControl.listOD(o.getId())) {
                    Drink d = Controller.DrinkControl.findByID(oD.getIdDrink());
                    modelOD.addRow(new Object[]{
                        d.getName(), oD.getQuantity(), oD.getNote(), d.getPrice() * oD.getQuantity(), oD.getPrice()
                    });
                }
                break;
            }
        }
        totalPay.setText(String.valueOf(Controller.OrderControl.getPrice(od)));
    }//GEN-LAST:event_jTableLOMouseClicked


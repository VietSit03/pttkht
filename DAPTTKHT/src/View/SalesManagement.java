    private void jTableOrderKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTableOrderKeyPressed
        // TODO add your handling code here:
        int vitri = jTableOrder.getSelectedRow();
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            modelO.setValueAt(tempPrice * Integer.parseInt(jTableOrder.getValueAt(vitri, 1).toString()), vitri, 2);
            modelO.setValueAt(tempPriceP * Integer.parseInt(jTableOrder.getValueAt(vitri, 1).toString()), vitri, 3);
        }
        if (Integer.parseInt(modelO.getValueAt(vitri, 1).toString()) == 0) {
            modelO.removeRow(vitri);
        }
    }//GEN-LAST:event_jTableOrderKeyPressed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean b = false;
        if (jNam.isSelected()) {
            b = Controller.EmployeeControl.add(jName.getText(), jIDCard.getText(), 1, jPosition.getItemAt(jPosition.getSelectedIndex()), jUsername.getText());
        } else {
            b = Controller.EmployeeControl.add(jName.getText(), jIDCard.getText(), 0, jPosition.getItemAt(jPosition.getSelectedIndex()), jUsername.getText());
        }
        if (b) {
            clear();
            showTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


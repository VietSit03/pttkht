    public void showTable() {
        int rowCount = modelE.getRowCount();
        // Xoá từng hàng một
        for (int i = rowCount - 1; i >= 0; i--) {
            modelE.removeRow(i);
        }
        emp = Controller.EmployeeControl.listEmp();
        for (Employee e : emp) {
            if (e.getGender() == 1) {
                modelE.addRow(new Object[]{
                    e.getId(), e.getName(), e.getIdCard(), "Nam", e.getPosition(), e.getUsername()
                });
            } else {
                modelE.addRow(new Object[]{
                    e.getId(), e.getName(), e.getIdCard(), "Nữ", e.getPosition(), e.getUsername()
                });
            }
        }
    }
    

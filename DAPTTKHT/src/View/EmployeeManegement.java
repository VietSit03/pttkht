    private void jTableEmpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEmpMouseClicked
        // TODO add your handling code here:
        int vitri = jTableEmp.getSelectedRow();
        jLabelID.setText((String) jTableEmp.getValueAt(vitri, 0));
        jName.setText((String) jTableEmp.getValueAt(vitri, 1));
        jIDCard.setText((String) jTableEmp.getValueAt(vitri, 2));
        if (jTableEmp.getValueAt(vitri, 3).equals("Nam")) {
            jNam.setSelected(true);
        } else {
            jNu.setSelected(true);
        }
        jPosition.setSelectedItem((String) jTableEmp.getValueAt(vitri, 4));
        jUsername.setText((String) jTableEmp.getValueAt(vitri, 5));
    }//GEN-LAST:event_jTableEmpMouseClicked

